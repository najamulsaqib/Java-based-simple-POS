    private void displayTable2(){
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dmdatabase","root","");
            Statement st = con.createStatement();
            String q = "SELECT `productname` as `Product Name`,`quantity` as `Quantity`, `price` as `Price` from billproduct where billid =  '"+newBillId+"';";
            Statement stmt = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = stmt.executeQuery(q);
            BillProducts.setModel(DbUtils.resultSetToTableModel(rs));
            DefaultTableModel tm = (DefaultTableModel)BillProducts.getModel();
            while(rs.next()){
                Object o[] = {rs.getString("`productname`"),rs.getString("quantity"),rs.getBigDecimal("price")};
                tm.addRow(o);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    private void billId(){
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dmdatabase","root","");
            Statement st = con.createStatement();
            String q = "select billid from bill ORDER BY billid DESC LIMIT 1";
            ResultSet rs = st.executeQuery(q);
            rs.beforeFirst();
            rs.next();
            int billid = rs.getInt("billid");
            newBillId = billid + 1;
            displayBillId.setText(Integer.toString(newBillId));
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
