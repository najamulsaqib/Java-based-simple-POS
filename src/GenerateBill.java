    private void billActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_billActionPerformed
        String Name, contact, addr;
        Name = name.getText();
        contact = contactno.getText();
        addr = address.getText();
        if(Name!=null){
            try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dmdatabase","root","");
                Statement st = con.createStatement();
                String q = "INSERT INTO `bill` (`billid`, `customerName`, `Customerphone`, `CustomerAddress`, `BillingDate`, `TotalBill`) VALUES ('"+newBillId+"', '"+Name+"', '"+contact+"', '"+addr+"', current_timestamp(), '"+total+"')";
                st.execute(q);
                BillDisplay bd = new BillDisplay(newBillId);
                this.dispose();
                bd.setVisible(true);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Write customer's Information.");
        }
    }//GEN-LAST:event_billActionPerformed

