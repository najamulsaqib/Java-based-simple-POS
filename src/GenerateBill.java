    private void invoiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_invoiceActionPerformed
        if(selectedPid!=null){
            quantity = Integer.parseInt(qty.getText());
            price = (double) (selectedPprice * quantity);
            try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dmdatabase","root","");
                Statement st = con.createStatement();
                String q = "INSERT INTO `billproduct` (`id`, `pid`, `billid`, `productname`, `quantity`, `price`) VALUES (NULL, '"+selectedPid+"', '"+newBillId+"', '"+selectedPname+"', '"+quantity+"', '"+price+"');";
                st.execute(q);
                String q2 = "SELECT `pstock` FROM `product` where productid = '"+selectedPid+"';";
                ResultSet rs = st.executeQuery(q2);
                rs.beforeFirst();
                rs.next();
                int pstock = rs.getInt("pstock");
                pstock = pstock - quantity;
                String q3 = "UPDATE `product` SET `pstock` = '"+pstock+"' WHERE `productid` = '"+selectedPid+"';";
                st.execute(q3);
                displayTable2();
                String q4 = "SELECT SUM(price) as `total` from billproduct WHERE billid = '"+newBillId+"';";
                ResultSet res = st.executeQuery(q4);
                res.beforeFirst();
                res.next();
                total = res.getDouble("total");
                totalPrice.setText(Double.toString(total));
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Select any item");
        }
    }//GEN-LAST:event_invoiceActionPerformed

